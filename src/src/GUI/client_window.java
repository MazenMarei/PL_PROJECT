    private void CartTapBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CartTapBtnMouseClicked
        // TODO add your handling code here:
        CartPanel.setVisible(true);
        SearchPanel.setVisible(false);
        UserSettingPanel.setVisible(false);
        

            DefaultTableModel model = (DefaultTableModel) CartTable.getModel();
            int totalPrice = 0;
            model.setRowCount(0);
            for (CartItem p : userCart.getItems()) {
                Product itemProduct =  Search.getProductByID(p.getProductId());
                Object[] row = {
                   p.getProductId(),
                   itemProduct.getName(),
                   itemProduct.getCategory(),
                   String.valueOf(itemProduct.getPrice()),
                   String.valueOf(p.getProductQuantity())
                };
                totalPrice = totalPrice + itemProduct.getPrice();
                model.addRow(row);
            }

            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

            // Apply the renderer to each column
            for (int i = 0; i < CartTable.getColumnCount(); i++) {
                CartTable.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }
            
            
            
            TotalPriceText.setText(String.valueOf(totalPrice));
        
    }//GEN-LAST:event_CartTapBtnMouseClicked

