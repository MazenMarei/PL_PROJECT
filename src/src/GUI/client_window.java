    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
            boolean updated = false;
        UserActions current_user = (UserActions) userModification.getCurrentUser();
        if (!CurrentName.getText().equals(current_user.getName())) {
            current_user.setName(CurrentName.getText());
            updated = true;
        }

        if (!currentEmail.getText().equals(current_user.getEmail())) {
            if (!currentEmail.getText().matches("^(.+)@(.+)$")) {
                JOptionPane.showMessageDialog(null, "You have to enter Email in good format like : email@domain.com");
            } else {
                current_user.setEmail(currentEmail.getText());
                updated = true;
            }
        }

        if (NewPassword.getText().length() > 0) {
            if (CurrentPassword.getText().length() < 1 || ((CurrentPassword.getText().length() > 0)
                    && !(CurrentPassword.getText().equals(current_user.getPassword())))) {
                JOptionPane.showMessageDialog(null, "You have To put your old password correct");
            } else if (CurrentPassword.getText().equals(current_user.getPassword())) {
                updated = true;
                current_user.setPassword(NewPassword.getText());
            }

        }

        if (!updated) {
            JOptionPane.showMessageDialog(null, "You have to edit at least one input to update.");
        } else {
            User.update(current_user);
            userModification.setCurrentUser((UserActions) current_user);
            
            UserSettingName.setText(userModification.getCurrentUser().getName());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

