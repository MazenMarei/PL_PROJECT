    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        // TODO add your handling code here:
        userModification Login_user = new userModification();

        try {
            boolean logedIn = Login_user.Login(Email.getText(), jPasswordField1.getText());
        if (logedIn) {
            UserActions currentUser = userModification.getCurrentUser();
            userModification.Role role = currentUser.getRole();
            close();
            
            switch (role) {
                case userModification.Role.ADMIN -> {
                try {
                    admin_window a1 = new admin_window();
                    a1.setVisible(true);
                    a1.setLocationRelativeTo(null);
                      } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex);
                    }
                    }
                case userModification.Role.INVENTORY -> {
                    
                    try {
                        user_window u1 = new user_window();
                        u1.setVisible(true);
                        u1.setLocationRelativeTo(null);
                    } catch (FileNotFoundException ex) {
                        JOptionPane.showMessageDialog(null, ex);
                    }
                    }
                case userModification.Role.CLIENT -> {
                     try {
                    client_window ClientForm = new client_window();
                    ClientForm.setVisible(true);
                    ClientForm.setLocationRelativeTo(null);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex);
                    }
                    }
                default ->  JOptionPane.showMessageDialog(null, "UNKOWEN ROLE!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ivalid email or password");
        }
        } catch (IllegalArgumentException e) {
             JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_LoginBtnActionPerformed

