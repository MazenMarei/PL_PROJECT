    private void LoginBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtn2ActionPerformed
        // TODO add your handling code here:
        String ReName = Name.getText();
        String RE_Email = ReEmail.getText();
        String Password = RePassword.getText();
        
        if((ReName == null || ReName.length() < 1) ||  (RE_Email == null || RE_Email.length() < 1) ||  (Password == null || Password.length() < 1)) {
            JOptionPane.showMessageDialog(null, "All Fields Are Requierd!");
            
        } else {
            
            userModification newUser = new userModification();
            try {
                newUser.Register(ReName, RE_Email, Password, userModification.Role.CLIENT);
                
                RegisterPanel.setVisible(false);
                LoginPanel.setVisible(true);
                
                
            } catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(null, e);
            }
            
        }
        
    }//GEN-LAST:event_LoginBtn2ActionPerformed

